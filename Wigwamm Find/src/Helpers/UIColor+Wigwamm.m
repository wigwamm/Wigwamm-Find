#import "UIColor+Wigwamm.h"

@implementation UIColor (Wigwamm)

#pragma mark - Orange

+ (UIColor *)w_orangeColor
{
    return [UIColor colorWithRed:0.99 green:0.36 blue:0.12 alpha:1];
}

+ (UIColor *)w_lightOrangeColor
{
    return [UIColor colorWithRed:1 green:0.87 blue:0.8 alpha:1];
}

#pragma mark - Green

+ (UIColor *)w_greenColor
{
    return [UIColor colorWithRed:0.16 green:0.83 blue:0.24 alpha:1];
}

#pragma mark - Gray

+ (UIColor *)w_grayColor
{
    return [UIColor colorWithRed:0.8 green:0.82 blue:0.86 alpha:1];
}

+ (UIColor *)w_lightGrayColor
{
    return [UIColor colorWithRed:0.88 green:0.89 blue:0.91 alpha:1];
}

+ (UIColor *)w_darkGrayColor
{
    return [UIColor colorWithRed:0.68 green:0.71 blue:0.74 alpha:1];
}

@end
